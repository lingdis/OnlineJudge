package basic;

public class LanguageParameterSpecific {
	
	int languageParameterSpecificId;
	String parameterConverterType;
	
	int sourceRawParameterPos;
	int languageParameterPos;
	
	public int getLanguageParameterSpecificId() {
		return languageParameterSpecificId;
	}
	public void setLanguageParameterSpecificId(int languageParameterSpecificId) {
		this.languageParameterSpecificId = languageParameterSpecificId;
	}
	public String getParameterConverterType() {
		return parameterConverterType;
	}
	public void setParameterConverterType(String parameterConverterType) {
		this.parameterConverterType = parameterConverterType;
	}
	public int getSourceRawParameterPos() {
		return sourceRawParameterPos;
	}
	public void setSourceRawParameterPos(int sourceRawParameterPos) {
		this.sourceRawParameterPos = sourceRawParameterPos;
	}
	public int getLanguageParameterPos() {
		return languageParameterPos;
	}
	public void setLanguageParameterPos(int languageParameterPos) {
		this.languageParameterPos = languageParameterPos;
	}
	
	
	
}
